#list declaration
a=[100,"yy",200,100,400,500,700,700,500,"hdhd"]
#print unique elements from list
print(set(a))
#index position zero means first element
print(a[0])
#index position first means second element
print(a[1])
#how to find the length of list
print(len(a))
#print every element from list one by one without manually using the index position 
for i in range(0,len(a)):
    print(i)
    print(a[i])


